-- 1. Find total spending by each customer. 
select c.customer_name, sum(o.order_amount) from customers as c
join orders as o on c.customer_id = o.customer_id
group by c.customer_name;

-- 2. Find customers whose total spending is greater than 2000.
select c.customer_name, sum(o.order_amount) from customers as c
join orders as o on c.customer_id = o.customer_id
group by c.customer_name 
having sum(o.order_amount)>2000;

-- 3. Find average order amount for each restaurant.
select r.restaurant_name, avg(o.order_amount) from restaurants as r
join orders as o on r.restaurant_id = o.restaurant_id
group by r.restaurant_name ;

-- 4. Categorize orders using CASE. Rules:
-- ● amount >= 1500 → Expensive
-- ● 800 to 1499 → Medium
-- ● below 800 → Budget 

select order_id, order_amount,
case
    when order_amount>=1500 then 'Expensive'
    when order_amount between 800 and 1499 then 'Medium'
    else 
        'Budget'
	end as category
from orders;

-- 5. Find customers who placed more than 1 order.
select c.customer_name, count(o.customer_id) as total_order from customers as c
join orders as o on c.customer_id = o.customer_id
group by c.customer_name
having count(o.customer_id)>1;

-- 6. Display customer level using CASE based on total spending.
-- Rules:
-- ● = 3000 → VIP
-- ● 1500 to 2999 → Premium
-- ● below 1500 → Regular
select c.customer_name, sum(o.order_amount) as total_spending ,
case
    when sum(o.order_amount)>=3000 then 'VIP'
    when sum(o.order_amount) between 1500 and 2999 then 'Premium'
    else
        'Regular'
	end as category
from customers as c join orders as o on c.customer_id = o.customer_id
group by c.customer_name;

-- 8 . Find highest order amount placed by each customer.
select c.customer_name, max(o.order_amount) as highest_amount from customers as c
join orders as o on c.customer_id = o.customer_id 
